<script>
    document.getElementById('hitungHarga').addEventListener('click', function() {
        var durasi = parseInt(document.getElementById('durasi').value) || 0;
        var jumlahPeserta = parseInt(document.getElementById('jumlahPeserta').value) || 0;
        var penginapan = document.getElementById('penginapan').checked ? 1000000 : 0;
        var transportasi = document.getElementById('transportasi').checked ? 1200000 : 0;
        var makanan = document.getElementById('makanan').checked ? 500000 : 0;

        var hargaPaket = penginapan + transportasi + makanan;
        var jumlahTagihan = hargaPaket * durasi * jumlahPeserta;

        document.getElementById('hargaPaket').textContent = 'Rp ' + hargaPaket.toLocaleString('id-ID');
        document.getElementById('jumlahTagihan').textContent = 'Rp ' + jumlahTagihan.toLocaleString('id-ID');
        document.getElementById('hasilPerhitungan').style.display = 'block';
    });
</script>